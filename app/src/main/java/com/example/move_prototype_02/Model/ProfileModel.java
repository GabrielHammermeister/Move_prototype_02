package com.example.move_prototype_02.Model;

public class ProfileModel {

    private String name;
    private int age;
    private Boolean sex;

    public ProfileModel() {    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public Boolean getSex() {
        return sex;
    }

    public void setSex(Boolean sex) {
        this.sex = sex;
    }
}
